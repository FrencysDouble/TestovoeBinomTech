object CustomIcons {

    @SuppressLint("ResourceType")
    fun createCustomMarker(context: Context, @DrawableRes userImage: Int): Bitmap {
        val markerView = LayoutInflater.from(context).inflate(R.drawable.custom_marker, null)

        val imageView = markerView.findViewById<de.hdodenhof.circleimageview.CircleImageView>(R.id.profile_image)
        imageView.setImageResource(userImage)

        markerView.measure(
            View.MeasureSpec.makeMeasureSpec(0, View.MeasureSpec.UNSPECIFIED),
            View.MeasureSpec.makeMeasureSpec(0, View.MeasureSpec.UNSPECIFIED)
        )
        markerView.layout(0, 0, markerView.measuredWidth, markerView.measuredHeight)

        val bitmap = Bitmap.createBitmap(markerView.measuredWidth, markerView.measuredHeight, Bitmap.Config.ARGB_8888)
        val canvas = Canvas(bitmap)
        markerView.draw(canvas)

        return bitmap
    }

    
}